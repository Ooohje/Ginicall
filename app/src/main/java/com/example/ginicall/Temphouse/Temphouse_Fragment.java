public class Temphouse_Fragment extends Fragment {
    private View view;
    private Context context;
    private KakaoMap kakaoMap;
    private LabelLayer labelLayer;
    private FirebaseFirestore db = FirebaseFirestore.getInstance();

    @Override
    public void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
    }

    @Override
    public View onCreateView(LayoutInflater inflater, ViewGroup container,
                             Bundle savedInstanceState) {
        view =  inflater.inflate(R.layout.fragment_temphouse_, container, false);
        MapView mapView = view.findViewById(R.id.temphouse_map_view);
        mapView.start(new MapLifeCycleCallback() {
            @Override
            public void onMapDestroy() {
                // 지도 API 가 정상적으로 종료될 때 호출됨
            }

            @Override
            public void onMapError(Exception error) {
                // 인증 실패 및 지도 사용 중 에러가 발생할 때 호출됨
            }
        }, new KakaoMapReadyCallback() {
            @Override
            public void onMapReady(KakaoMap map) {
                // 인증 후 API 가 정상적으로 실행될 때 호출됨
                kakaoMap = map;
                labelLayer = kakaoMap.getLabelManager().getLayer();
                moveCamera(LatLng.from(35.88900, 128.6103));
            }
        });
        return view;
    }

    private void moveCamera(LatLng position) {
        kakaoMap.moveCamera(CameraUpdateFactory.newCenterPosition(position), CameraAnimation.from(10, false,false));
    }
}